# by default seperator is space, sep=" "
print("Hello", "world", "program", 3.14)

# use sepearator as -, sep="-"
print("Hello", "world", "program", 3.14, sep="-")

# seperate with pipe sep="|"
print("Hello", "world", "program", 3.14, sep="|")

# by default end with newline, end="\n"
print("hello")
print("python")

# with space end=" "
print("hello", end=" ")
print("python")

# with sep and end
print("Hi", "hello", sep="|", end="_")
print("good morning", sep="|")

print("Good", "morning", sep="?", end="-")
print("World")
